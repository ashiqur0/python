#### Change List Item ####
# Change Item Value
fruits = ['apple', 'banana', 'cherry']

# Change Second Item
fruits[1] = 'blackcurrant'
print(fruits)

# Change Range of Item values
fruits = ['apple', 'banana', 'cherry', 'orange', 'kiwi', 'mango']
fruits[1:3] = ['blackcurrant', 'watermelon']
print(fruits)

# Change Second value by Replacing it with two new value
fruits = ['apple', 'banana', 'cherry']
fruits[1:2] = ['blackcurrant', 'watermelon']
print(fruits)

# Change second and third value by replacing with one value
fruits[1:3] = ['banana']
print(fruits)

# Insert Items
fruits = ['apple', 'banana', 'cherry']
fruits.insert(2, 'orange')
print(fruits)

