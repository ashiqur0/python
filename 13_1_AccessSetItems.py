#### Python Access Set Items ####

# Access Item : is not possible in set but
fruitSet = {'apple', 'banana', 'cherry'}
print(fruitSet)

# Can access through for loop
for x in fruitSet:
    print(x)

# Can check if an item is present in a set
print('apple' in fruitSet)

# Check if 'apple' is present or not in the set
print('apple' not in fruitSet)

