#### Output Variables ####

x = "Python is awesome"
print(x)

# Output multiple variable separated by comma
x = "Python"
y = "is"
z = "awesome"
print(x, y, z)

# + operator to output multiple variable
x = "python "
y = "is "
z = "awesome"
print(x + y + z)

# For numbers '+' operators works as mathmatical operator
x = 5
y = 6
print(x + y)    # 11

# Number and String can not execute in print() using '+' operator
x = 5
y = "John"
# print(x + y) # Error

# But it is possible by using ',' comma
print(x, y)