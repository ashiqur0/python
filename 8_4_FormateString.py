################ Format String ################

# F-String 
age = 36
txt = f"My name is John. I am {age}"
print(txt)

# Placeholder and Modifiers
price = 99
txt = f"The price is {price:.2f} dollars"   # Display the price with 2 decimal
print(txt)

# Perform math operation in the placeholder and return the result
txt = f"The price is {20*3} dollars"
print(txt)