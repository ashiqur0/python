################ Python Escape Character ################
print("\'")             # Single Quote
print("\\")             # Double Quote
print('\n')             # New line
print("Hello\rWorld")   # Carriage Return
print("a\tf")           # Tab
print("Hello \bWorld")  # Backspace
print("\110\145\154\154\157")   # Octal value
print("\x48\x65\x6c\x6c\x6f")   # Hex value