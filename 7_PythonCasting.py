################ Python Casting ################
# Integers 
x = int(1)      # Will be 1
y = int(2.8)    # Will be 2
z = int("3")    # Will be 3

# Float 
w = float(1)    # Will be 1.0
x = float(2.8)  # Will be 2.8
y = float("3")  # Will be 3.0
z = float("4.2")# Will be 4.2

# Strings
x = str("s1")   # Will be 's1'
y = str(2)      # Will be '2'
z = str(3.0)    # Will be '3.0'