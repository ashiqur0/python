################ Modify String ################

# Upper Case
a = "Hello World"
print(a.upper())

# Lower Case
print(a.lower())

# Remove Whitespace
a = "   Hello World!    "
print(a.strip())        # returns 'Hello World!'

# Replacing Strign
a = "Hello World!"
print(a.replace("H", "J"))

# Split Strign
a = "Hello, World!"
b = a.split(",")    # b = ['Hello', "World!"]
print(b)

