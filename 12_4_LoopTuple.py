#### Loop Tuple ####

# Itarate Through the Items and Print the Value
fruits = ('apple', 'banana', 'cherry')
for x in fruits:
    print(x)

# Loop Through the Index Number
for x in range(len(fruits)):
    print(fruits[x])

# Using a while loop
i = 0
while i < len(fruits):
    print(fruits[i])
    i += 1

